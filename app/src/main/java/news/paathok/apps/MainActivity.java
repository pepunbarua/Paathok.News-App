    public class DownloadTask extends AsyncTask<String, Void, String> {

        @Override
        protected String doInBackground(String... strings) {

            String result = "";
            URL url;
            HttpURLConnection urlConnection = null;

            try {

                url = new URL(strings[0]);

                urlConnection = (HttpURLConnection) url.openConnection();

                InputStream in = urlConnection.getInputStream();

                InputStreamReader reader = new InputStreamReader(in);

                int data = reader.read();

                while (data !=-1){

                    char current = (char) data;

                    result += current;

                    data = reader.read();
                }

                ///Log.i("URLContent", result);

                ///JSONArray jsonArray = new JSONArray(result);

                JSONObject newsIdObject = new JSONObject(result);

                JSONArray newsArray = newsIdObject.getJSONArray("posts");

                for (int i = 0; i < newsArray.length(); i++) {

                    JSONObject newsProperty = newsArray.getJSONObject(i);

                    Log.i("JSONItem", newsProperty.getString("ID"));
                    Log.i("JSONItem", newsProperty.getString("title"));

                }


            } catch (MalformedURLException e) {
                e.printStackTrace();
            } catch (IOException e) {
                e.printStackTrace();
            } catch (JSONException e) {
                e.printStackTrace();
            }


            return null;
        }
    }
